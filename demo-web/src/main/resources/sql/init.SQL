CREATE TABLE `test`.`USER_DETAIL`(  
  `UD_USER_ID` INT(20) NOT NULL AUTO_INCREMENT COMMENT '用户ID',
  `UD_USER_NAME` VARCHAR(20) COMMENT '用户姓名',
  `UD_USER_LOGIN_NAME` VARCHAR(20) COMMENT '用户登录名',
  `UD_USER_LOGIN_PASSWORD` VARCHAR(20) COMMENT '用户登录密码',
  `UD_USER_STATUS` INT(1) DEFAULT 1 COMMENT '用户状态',
  `UD_USER_LOGIN_STATUS` INT(1) COMMENT '用户是否登录',
  `UD_USER_PURVIEW` INT(20) COMMENT '用户权限',
  PRIMARY KEY (`UD_USER_ID`)
) CHARSET=utf8 COLLATE=utf8_bin;

